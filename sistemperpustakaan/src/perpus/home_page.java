    private void tampil_data() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No");
        model.addColumn("ID Buku");
        model.addColumn("Judul Buku");
        model.addColumn("Peminjam");
        model.addColumn("Pengarang");
        model.addColumn("Penerbit");
        model.addColumn("Tgl Pinjam");
        model.addColumn("Tgl Kembali");
        
        String cari = txtcari.getText();
        try {
            int i = 1;
            String sql = "SELECT * FROM tbbuku WHERE idBuku LIKE '%"+cari+"%'";
            java.sql.Connection conn = (Connection)config.congfigDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            
            while (res.next()) {
                model.addRow(new Object[]{i++, res.getString(1), res.getString(2), res.getString(3), res.getString(4), res.getString(5), res.getString(6), res.getString(7)});
            }
            tblbuku.setModel(model);
            
        } catch (SQLException e) {
            System.out.println("Error : " + e.getMessage());
        }
    }
    
    public static Date getTanggalFromtTable(JTable table, int kolom) {
        JTable tabel = table;
        String strtgl = String.valueOf(tabel.getValueAt(tabel.getSelectedRow(), kolom));
        Date tanggal = null;
        try {
            tanggal = new SimpleDateFormat("yyyy-MM-dd").parse(strtgl);
        } catch (ParseException ex) {
            Logger.getLogger(home_page.class.getName()).log(Level.SEVERE, null, ex);
        }
        return tanggal;
    }

