    private void tblbukuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblbukuMouseClicked
        // TODO add your handling code here:
        
        int baris = tblbuku.rowAtPoint(evt.getPoint());
        txtidbuku.disable();
        String idBuku = tblbuku.getValueAt(baris, 1).toString();
        txtidbuku.setText(idBuku);
        String judulBuku = tblbuku.getValueAt(baris, 2).toString();
        txtjudul.setText(judulBuku);
        String peminjam = tblbuku.getValueAt(baris, 3).toString();
        txtpeminjam.setText(peminjam);
        String pengarang = tblbuku.getValueAt(baris, 4).toString();
        txtpengarang.setText(pengarang);
        String penerbit = tblbuku.getValueAt(baris, 5).toString();
        txtpenerbit.setText(penerbit);
        txttglpinj.setDate(getTanggalFromtTable(tblbuku, 6));
        txttglkemb.setDate(getTanggalFromtTable(tblbuku, 7));
        
    }//GEN-LAST:event_tblbukuMouseClicked

    private void simpanbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanbtnActionPerformed
        // TODO add your handling code here
        if (txtjudul.getText().equals("") || txtpengarang.getText().equals("") || txtpeminjam.getText().equals("") || txtpenerbit.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Isi Semua Data ", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            String formattgl = "yyyy-MM-dd";
            SimpleDateFormat frmt = new SimpleDateFormat(formattgl);
            String tanggalpinj = String.valueOf(frmt.format(txttglpinj.getDate()));
            String tanggalkemb = String.valueOf(frmt.format(txttglkemb.getDate()));
            try {
                String sql = "INSERT INTO tbbuku VALUES ('" +txtidbuku.getText()+ "', '" +txtjudul.getText()+ "', '" +txtpeminjam.getText()+ "', '" +txtpengarang.getText()+ "', '" +txtpenerbit.getText()+ "', '" +tanggalpinj+ "', '" +tanggalkemb+ "');"; 
                java.sql.Connection conn = (Connection)config.congfigDB();
                java.sql.PreparedStatement preps = conn.prepareStatement(sql);
                preps.execute();
                JOptionPane.showMessageDialog(null, "Berhasil menyimpan data");
                
                tampil_data();
                bersih();
            } catch (HeadlessException | SQLException e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), "Err", JOptionPane.ERROR_MESSAGE);
            } 
        } 
    }//GEN-LAST:event_simpanbtnActionPerformed

